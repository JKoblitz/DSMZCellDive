    <p class="d-none">
        The public cell line collection of the Human
        and Animal Cell Line department at DSMZ today holds more than 860 cell lines
        available for the research community. Of these about 600 are of human
        origin and about 260 of animal origin. Human cell lines are mostly
        derived from malignant tumour material donated by patients. Cell lines
        serve as <em>in vitro</em> models for the respective diseases and are of
        importance for understanding the molecular changes underlying human
        disease. In the past 10 years high-throughput sequencing techniques
        became available and enabled in depth analysis of the cellular genome
        and transcriptomes. In 2016 we started to perform RNA-seq of all human
        cell lines included in the open cell line collection. DSMZCellDive
        offers analysis tools like barplots and heatmaps to plot gene
        expression leves in cell lines of choice, which are presented
        according to cell type/tumour entity. This enables researchers to
        decide on the best appropriate model for their research questions.
    </p>
